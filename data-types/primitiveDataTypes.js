// ************ PRIMITIVE DATA TYPES ***************
// example 1 number
let age = 25;
let temperature = 98.6;
console.log(age);
console.log(temperature);

// example 2 string
let name = 'John Doe';
let message = "Hello, world!";
console.log(name);
console.log(message);

// example 3 boolean
let isTrue = true;
let isFalse = false;
console.log(isTrue);
console.log(isFalse);

// example 4 Null
let myValue = null;
console.log(myValue);

// example 5 undefined
let myVariable;
let otherVariable = undefined;
console.log(myVariable);
console.log(otherVariable);

// example 6 symbol 
let id = Symbol('uniqueId');
console.log(id);